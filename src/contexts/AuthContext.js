import React, { createContext, useContext, useState, useEffect } from 'react';
import auth from '@react-native-firebase/auth';
import firestore from '@react-native-firebase/firestore';
import AsyncStorage from '@react-native-async-storage/async-storage';

export const AuthContext = createContext();

const IS_DEV = __DEV__;
const log = (...args) => IS_DEV && console.log(...args);
const logError = (...args) => console.error(...args);

// Batch AsyncStorage operations
const batchAsyncStorage = {
  async setMultiple(items) {
    return AsyncStorage.multiSet(items);
  },
  async removeMultiple(keys) {
    return AsyncStorage.multiRemove(keys);
  }
};

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [loading, setLoading] = useState(true);
  const [isGuest, setIsGuest] = useState(false);
  const [guestData, setGuestData] = useState(null);

  // Initialisation Firebase Auth
  useEffect(() => {
    log('üîÑ Initialisation Firebase Auth');
    
    // Charger le mode guest
    const loadGuestMode = async () => {
      try {
        const guestMode = await AsyncStorage.getItem('@fitnessrpg:guest_mode');
        const savedGuestData = await AsyncStorage.getItem('@fitnessrpg:guest_data');
        
        if (guestMode === 'true') {
          log('üë§ Mode invit√© actif');
          setIsGuest(true);
          if (savedGuestData) {
            setGuestData(JSON.parse(savedGuestData));
          }
        }
      } catch (error) {
        console.error('‚ùå Erreur chargement guest mode:', error);
      }
    };
    
    loadGuestMode();

    // √âcouter les changements d'authentification
    const unsubscribe = auth().onAuthStateChanged(async (firebaseUser) => {
      if (firebaseUser) {
        log('‚úÖ Utilisateur connect√©:', firebaseUser.email || firebaseUser.phoneNumber);
        setUser(firebaseUser);
        setIsGuest(false);
        
        // V√©rifier que le document existe (backup - normalement d√©j√† cr√©√© par login/signup)
        try {
          const doc = await firestore()
            .collection('users')
            .doc(firebaseUser.uid)
            .get();
          
          if (!doc.exists) {
            log('üìù Cr√©ation du document utilisateur (onAuthStateChanged backup)');
            await firestore()
              .collection('users')
              .doc(firebaseUser.uid)
              .set({
                email: firebaseUser.email || null,
                phoneNumber: firebaseUser.phoneNumber || null,
                totalXP: 0,
                level: 1,
                completedPrograms: [],
                userProgress: {},
                activePrograms: [],
                selectedPrograms: [],
                streak: 0,
                lastWorkoutDate: null,
                totalChallengesSubmitted: 0,
                totalChallengesApproved: 0,
                lastSubmissionDate: null,
                createdAt: firestore.FieldValue.serverTimestamp(),
              });
          }
        } catch (error) {
          console.error('‚ö†Ô∏è Erreur document:', error);
          // Non bloquant - l'utilisateur peut continuer
        }
      } else {
        log('‚ÑπÔ∏è Aucun utilisateur connect√©');
        setUser(null);
      }
      
      setLoading(false);
    });

    return () => unsubscribe();
  }, []);

  // SIGNUP
  const signup = async (email, password) => {
    try {
      log('üìù Inscription:', email);
      
      if (!email || !password) {
        return { 
          success: false, 
          error: 'Email et mot de passe requis',
          code: 'validation/missing-fields'
        };
      }
      
      if (password.length < 6) {
        return {
          success: false,
          error: 'Le mot de passe doit contenir au moins 6 caract√®res',
          code: 'auth/weak-password'
        };
      }
      
      // Cr√©er le compte
      const userCredential = await auth().createUserWithEmailAndPassword(email, password);
      const newUser = userCredential.user;
      
      // Cr√©er le document Firestore
      await firestore()
        .collection('users')
        .doc(newUser.uid)
        .set({
          email: newUser.email,
          totalXP: 0,
          level: 1,
          completedPrograms: [],
          userProgress: {},
          activePrograms: [],      // ‚≠ê CORRECTION
          selectedPrograms: [],    // ‚≠ê CORRECTION
          streak: 0,
          lastWorkoutDate: null,
          totalChallengesSubmitted: 0,
          totalChallengesApproved: 0,
          lastSubmissionDate: null,
          createdAt: firestore.FieldValue.serverTimestamp(),
        });
      
      // Nettoyer le mode invit√©
      await AsyncStorage.multiRemove([
        '@fitnessrpg:guest_mode',
        '@fitnessrpg:guest_data'
      ]);
      await AsyncStorage.setItem('@fitnessrpg:onboarding_completed', 'true');
      
      setIsGuest(false);
      setGuestData(null);
      
      log('‚úÖ Inscription r√©ussie');
      
      return { success: true, user: newUser };
      
    } catch (error) {
      console.error('‚ùå Erreur inscription:', error);
      
      const errorMessages = {
        'auth/email-already-in-use': 'Cet email est d√©j√† utilis√©',
        'auth/invalid-email': 'Email invalide',
        'auth/weak-password': 'Mot de passe trop faible (min. 6 caract√®res)',
        'auth/network-request-failed': 'Erreur r√©seau. V√©rifiez votre connexion.',
        'auth/too-many-requests': 'Trop de tentatives. R√©essayez plus tard.',
      };
      
      return { 
        success: false, 
        error: errorMessages[error.code] || error.message || 'Erreur lors de l\'inscription',
        code: error.code 
      };
    }
  };

  // PHONE AUTH - ENVOYER CODE SMS
  const sendVerificationCode = async (phoneNumber) => {
    try {
      log('üì± Envoi code SMS √†:', phoneNumber);
      
      if (!phoneNumber || phoneNumber.length < 9) {
        return {
          success: false,
          error: 'Num√©ro de t√©l√©phone invalide',
          code: 'validation/invalid-phone'
        };
      }
      
      // Formater le num√©ro (ajouter +33 si n√©cessaire)
      let formattedPhone = phoneNumber.trim().replace(/\s/g, '');
      
      // Si commence pas par +, ajouter +33
      if (!formattedPhone.startsWith('+')) {
        // Si commence par 0, remplacer par +33
        if (formattedPhone.startsWith('0')) {
          formattedPhone = '+33' + formattedPhone.substring(1);
        } else {
          // Sinon (ex: 612345678), ajouter directement +33
          formattedPhone = '+33' + formattedPhone;
        }
      }
      
      log('üì± Num√©ro format√©:', formattedPhone);
      
      // V√©rifier que Firebase est initialis√©
      const app = auth().app;
      log('üî• Firebase app:', app ? app.name : 'NOT INITIALIZED');
      
      if (!app) {
        throw new Error('Firebase not initialized');
      }
      
      // Appel Firebase avec timeout de 30s
      log('‚è≥ Appel Firebase signInWithPhoneNumber...');
      const signInPromise = auth().signInWithPhoneNumber(formattedPhone);
      const timeoutPromise = new Promise((_, reject) => 
        setTimeout(() => reject(new Error('Firebase timeout apr√®s 30s')), 30000)
      );
      
      const confirmation = await Promise.race([signInPromise, timeoutPromise]);
      
      log('‚úÖ Code SMS envoy√©');
      
      return {
        success: true,
        confirmation,
        phoneNumber: formattedPhone
      };
      
    } catch (error) {
      logError('‚ùå Erreur envoi SMS:', error);
      
      let errorMessage = 'Erreur lors de l\'envoi du code';
      if (error.code === 'auth/invalid-phone-number') {
        errorMessage = 'Num√©ro de t√©l√©phone invalide';
      } else if (error.code === 'auth/too-many-requests') {
        errorMessage = 'Trop de tentatives. R√©essayez plus tard.';
      }
      
      return {
        success: false,
        error: errorMessage,
        code: error.code
      };
    }
  };

  // PHONE AUTH - V√âRIFIER CODE SMS
  const verifyCode = async (confirmation, code) => {
    try {
      log('üîê V√©rification code SMS:', code);
      
      if (!code || code.length !== 6) {
        return {
          success: false,
          error: 'Code invalide (6 chiffres requis)',
          code: 'validation/invalid-code'
        };
      }
      
      const userCredential = await confirmation.confirm(code);
      const loggedUser = userCredential.user;
      
      log('‚úÖ Code valid√©, utilisateur connect√©:', loggedUser.uid);
      
      // Cr√©er/mettre √† jour le document utilisateur
      try {
        const userDoc = await firestore()
          .collection('users')
          .doc(loggedUser.uid)
          .get();
        
        if (!userDoc.exists) {
          log('üìù Cr√©ation document utilisateur...');
          
          // R√©cup√©rer les donn√©es invit√© si elles existent
          const guestDataStr = await AsyncStorage.getItem('@fitnessrpg:guest_data');
          const guestData = guestDataStr ? JSON.parse(guestDataStr) : null;
          
          await firestore()
            .collection('users')
            .doc(loggedUser.uid)
            .set({
              phoneNumber: loggedUser.phoneNumber,
              totalXP: guestData?.totalXP || 0,
              level: guestData?.level || 1,
              completedPrograms: guestData?.completedPrograms || [],
              userProgress: guestData?.userProgress || {},
              activePrograms: guestData?.activePrograms || [],
              selectedPrograms: guestData?.selectedPrograms || [],
              streak: guestData?.streak || 0,
              lastWorkoutDate: guestData?.lastWorkoutDate || null,
              totalChallengesSubmitted: 0,
              totalChallengesApproved: 0,
              lastSubmissionDate: null,
              createdAt: firestore.FieldValue.serverTimestamp(),
            });
          log('‚úÖ Document cr√©√© avec donn√©es invit√©');
        } else {
          log('‚úÖ Document existe d√©j√†');
        }
      } catch (firestoreError) {
        logError('‚ö†Ô∏è Erreur Firestore (non bloquant):', firestoreError);
      }
      
      // Nettoyer le mode invit√©
      await AsyncStorage.multiRemove([
        '@fitnessrpg:guest_mode',
        '@fitnessrpg:guest_data'
      ]);
      await AsyncStorage.setItem('@fitnessrpg:onboarding_completed', 'true');
      
      setIsGuest(false);
      setGuestData(null);
      
      log('‚úÖ CONNEXION COMPLETE');
      
      return { success: true, user: loggedUser };
      
    } catch (error) {
      logError('‚ùå Erreur v√©rification code:', error);
      
      let errorMessage = 'Code incorrect';
      if (error.code === 'auth/invalid-verification-code') {
        errorMessage = 'Code invalide ou expir√©';
      } else if (error.code === 'auth/code-expired') {
        errorMessage = 'Code expir√©. Demandez-en un nouveau.';
      }
      
      return {
        success: false,
        error: errorMessage,
        code: error.code
      };
    }
  };

  // LOGIN (OLD - KEEP FOR COMPATIBILITY)
  const login = async (email, password) => {
    try {
      log('üîê Login START (EMAIL - DEPRECATED):', email);
      
      // ÔøΩ EMAIL AUTH NE MARCHE PAS - TIMEOUT
      // Redirige vers Phone Auth √† la place
      return {
        success: false,
        error: 'Email auth temporairement indisponible. Utilise Phone Auth.',
        code: 'auth/email-deprecated',
        usePhoneAuth: true
      };
      
    } catch (error) {
      console.error('‚ùå Erreur connexion:', error);
      
      const errorMessages = {
        'auth/user-not-found': 'Aucun compte avec cet email',
        'auth/wrong-password': 'Mot de passe incorrect',
        'auth/invalid-email': 'Email invalide',
        'auth/user-disabled': 'Ce compte a √©t√© d√©sactiv√©',
        'auth/network-request-failed': 'Erreur r√©seau. V√©rifiez votre connexion.',
        'auth/too-many-requests': 'Trop de tentatives. R√©essayez plus tard.',
        'auth/invalid-credential': 'Email ou mot de passe incorrect',
      };
      
      return { 
        success: false, 
        error: errorMessages[error.code] || error.message || 'Erreur lors de la connexion',
        code: error.code 
      };
    }
  };

  // RESET PASSWORD
  const resetPassword = async (email) => {
    try {
      log('üîë Reset password pour:', email);
      
      if (!email) {
        return {
          success: false,
          error: 'Email requis',
          code: 'validation/missing-email'
        };
      }
      
      await auth().sendPasswordResetEmail(email);
      
      log('‚úÖ Email de reset envoy√©');
      
      return { 
        success: true,
        message: 'Email de r√©initialisation envoy√©'
      };
      
    } catch (error) {
      console.error('‚ùå Erreur reset password:', error);
      
      const errorMessages = {
        'auth/user-not-found': 'Aucun compte associ√© √† cet email',
        'auth/invalid-email': 'Email invalide',
        'auth/network-request-failed': 'Erreur r√©seau. V√©rifiez votre connexion.',
      };
      
      return {
        success: false,
        error: errorMessages[error.code] || error.message || 'Erreur lors de l\'envoi',
        code: error.code
      };
    }
  };

  // LOGOUT
  const logout = async () => {
    try {
      setLoading(true);
      log('üîÑ D√©connexion...');
      
      await auth().signOut();
      
      await AsyncStorage.multiRemove([
        '@fitnessrpg:onboarding_completed',
        '@fitnessrpg:guest_mode',
        '@fitnessrpg:guest_data',
        '@fitnessrpg:tree_tooltip_shown'
      ]);
      
      setUser(null);
      setIsGuest(false);
      setGuestData(null);
      
      log('‚úÖ D√©connexion r√©ussie');
      
      return { success: true };
      
    } catch (error) {
      console.error('‚ùå Erreur d√©connexion:', error);
      return { success: false, error: error.message };
    } finally {
      setLoading(false);
    }
  };

  // MODE INVIT√â
  const setGuestMode = async () => {
    try {
      log('üë§ Activation mode invit√©');
      setIsGuest(true);
      await AsyncStorage.setItem('@fitnessrpg:guest_mode', 'true');
      return { success: true };
    } catch (error) {
      console.error('‚ùå Erreur mode invit√©:', error);
      return { success: false, error: error.message };
    }
  };

  const saveGuestData = async (data) => {
    try {
      setGuestData(data);
      await AsyncStorage.setItem('@fitnessrpg:guest_data', JSON.stringify(data));
      return { success: true };
    } catch (error) {
      console.error('‚ùå Erreur sauvegarde guest:', error);
      return { success: false, error: error.message };
    }
  };

  // CONVERSION INVIT√â ‚Üí UTILISATEUR
  const convertGuestToUser = async (email, password) => {
    try {
      log('üîÑ Conversion invit√© ‚Üí utilisateur:', email);
      
      // Validation
      if (!email || !password) {
        return { 
          success: false, 
          error: 'Email et mot de passe requis',
          code: 'validation/missing-fields'
        };
      }
      
      // V√©rification de l'existence de l'email (SANS TIMEOUT)
      log('üîç V√©rification email...', email);
      try {
        const signInMethods = await auth().fetchSignInMethodsForEmail(email);
        
        if (signInMethods && signInMethods.length > 0) {
          log('‚ùå Email d√©j√† utilis√©');
          return {
            success: false,
            error: 'Cet email est d√©j√† utilis√©. Connecte-toi plut√¥t !',
            code: 'auth/email-already-in-use'
          };
        }
        log('‚úÖ Email disponible');
      } catch (emailCheckError) {
        log('‚ö†Ô∏è Impossible de v√©rifier email, on continue quand m√™me...', emailCheckError.message);
        // Continue anyway - Firebase will catch duplicate email during signup
      }
      
      // Cr√©ation du compte (SANS TIMEOUT)
      log('üîê Cr√©ation du compte Firebase...');
      const createAccountPromise = auth().createUserWithEmailAndPassword(email, password);
      
      const [userCredential] = await Promise.all([
        createAccountPromise,
        AsyncStorage.setItem('@fitnessrpg:onboarding_completed', 'true')
      ]);
      
      log('‚úÖ Compte cr√©√©:', userCredential.user.uid);
      const newUser = userCredential.user;
      
      // Migrer les donn√©es du guest (non-bloquant)
      const guestDataToMigrate = guestData || {};
      firestore()
        .collection('users')
        .doc(newUser.uid)
        .set({
          email: newUser.email,
          totalXP: 0,
          level: 1,
          completedPrograms: [],
          userProgress: {},
          activePrograms: [],      // ‚≠ê CORRECTION
          selectedPrograms: [],    // ‚≠ê CORRECTION
          streak: 0,
          lastWorkoutDate: null,
          totalChallengesSubmitted: 0,
          totalChallengesApproved: 0,
          lastSubmissionDate: null,
          createdAt: firestore.FieldValue.serverTimestamp(),
          ...guestDataToMigrate,
        })
        .catch((error) => {
          logError('‚ö†Ô∏è Migration sera compl√©t√©e plus tard:', error.code);
        });
      
      // Nettoyer le guest mode
      await batchAsyncStorage.removeMultiple([
        '@fitnessrpg:guest_mode',
        '@fitnessrpg:guest_data'
      ]);
      
      setIsGuest(false);
      setGuestData(null);
      
      log('‚úÖ Conversion r√©ussie');
      
      return { success: true, user: newUser };
      
    } catch (error) {
      logError('‚ùå Erreur conversion:', error);
      
      const errorMap = {
        'auth/email-already-in-use': 'Cet email est d√©j√† utilis√©',
        'auth/invalid-email': 'Email invalide',
        'auth/weak-password': 'Mot de passe trop faible (min. 6 caract√®res)',
        'auth/network-request-failed': 'Erreur r√©seau. V√©rifiez votre connexion.',
      };
      
      return { 
        success: false, 
        error: errorMap[error.code] || 'Erreur lors de la cr√©ation du compte',
        code: error.code 
      };
    }
  };

  // RESET PROFIL (supprime toutes les donn√©es utilisateur)
  const resetUserData = async () => {
    try {
      log('üîÑ R√©initialisation du profil...');
      
      // 1. Supprimer le document Firestore si utilisateur authentifi√©
      if (user && !isGuest) {
        try {
          log('üóëÔ∏è Suppression document Firestore...');
          await firestore()
            .collection('users')
            .doc(user.uid)
            .delete();
          log('‚úÖ Document Firestore supprim√©');
        } catch (error) {
          logError('‚ö†Ô∏è Erreur suppression Firestore:', error.code);
          // Continue quand m√™me
        }
      }
      
      // 2. Nettoyer AsyncStorage
      await batchAsyncStorage.removeMultiple([
        '@fitnessrpg:onboarding_completed',
        '@fitnessrpg:guest_mode',
        '@fitnessrpg:guest_data',
        '@fitnessrpg:guest_programs',
        '@fitnessrpg:tree_tooltip_shown',
        '@fitnessrpg:open_program_selection'
      ]);
      
      // 3. Reset √©tats
      setGuestData(null);
      setIsGuest(false);
      
      log('‚úÖ Profil r√©initialis√©');
      
      return { success: true };
    } catch (error) {
      logError('‚ùå Erreur reset profil:', error);
      return { 
        success: false, 
        error: 'Erreur lors de la r√©initialisation' 
      };
    }
  };

  return (
    <AuthContext.Provider value={{ 
      user, 
      loading, 
      isGuest,
      guestData,
      sendVerificationCode,
      verifyCode,
      signup, 
      login, 
      logout,
      resetPassword,
      setGuestMode,
      saveGuestData,
      convertGuestToUser,
      resetUserData,
    }}>
      {children}
    </AuthContext.Provider>
  );
};

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};